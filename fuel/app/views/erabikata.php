			<h2 class="hd1 dgray full sp_mb_0">オンライン英会話をはじめる前に</h2>
			<figure class="full"><img src="images/erabikata/fig_before_start.jpg"></figure>
			<section id="intro1" class="shorten">
				<h3>英語はかなり難しい</h3>
				<p>私自身の話ですが、英語の勉強は3年くらいでやめるつもりでした。正直毎日30分程度話せば1〜2年でペラペラに話せるようになると思っていたからです。しかし3年くらいでは全然満足に話せませんでした。こんなに英語が難しいとは思っていませんでした。</p>
				<p>あなたが英語を話せるようになりたいと思うなら、留学するのが一番良いと思います。しかしもしそれが難しいなら、毎日30分の勉強を何年も続ける覚悟が必要と思います。個人差はあると思いますが、日本にいながら英語を話せるようになるのはかなり時間がかかると思って下さい。</p>
			</section>
			<section class="shorten">
				<h3>価格のみで決めない</h3>
				<p>オンライン英会話スクールを決めるにあたっては、価格と同じくらいサービスの質が重要と思います。もちろん値段とのバランスはあると思いますが、私はここを一番重視してきました。お気に入りの講師を発見したり、自分にあったテキストを見つけたりと、スクールに慣れるには時間がかかります。価格を重視して選んだために、サービスに満足できず、スクールを変えるとなると、次のスクールを探す時間、スクールに慣れる時間が本当に無駄です。価格と同じくらいサービスの質を重視したいところです。</p>
			</section>
			<section id="nonnative" class="shorten wfig">
				<h3>非ネイティブ講師はダメなのか？</h3>
				<p>多くのスクールはフィリピン講師を採用しています。では非ネイティブ講師はダメなのか？もちろんネイティブ講師に習う方が良いと思いますが、非ネイティブでも全く問題ないと思います。まず非ネイティブと言っても、彼らの英語の能力は非常に高く、あなたの英語のレベルが高くなければネイティブとの違いが分からないと思います。</p>
				<figure><img src="images/erabikata/fig_non_native.jpg"></figure>
				<section>
					<h4>英語を話す人の多くは非ネイティブ</h4>
					<p>そして実際に街で外国人と英語を話すと非ネイティブの人だらけなんです。さらにネイティブと言っても話し方や訛りや音量は人それぞれです。英語の教材のように、きれいな聞きとりやすい英語を話す人なんて滅多にいません。あなたの目標が「英語を話せるようになること」なら、いろんな環境でも聞けるようになることが大事なんです。</p>
					<p>時々「非ネイティブ講師でちゃんとした英語が身につくのか？」的な事を言う人がいます。しかしネイティブの講師や英語を話せる人にその質問をしたところ、誰からもネガティブな意見は帰ってきませんでした。私の印象では英語を全く話せない人ほどネイティブにこだわっているように思います。</p>
				</section>
				<section>
					<h4>ネイティブとの会話も有意義</h4>
					<p>私の場合、オンライン英会話は全て非ネイティブですが、週に一回ネイティブの人と話すようにしています。ネイティブの発音を確認できるだけでなく、表現や慣習などネイティブスピーカーとの会話は有意義です。英会話カフェや、チケットレッスン制の英会話スクールなどでたまにネイティブと話すのはおすすめです。</p>
				</section>
			</section>
