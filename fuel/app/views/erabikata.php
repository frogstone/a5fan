				<h3>キッズの場合は専用のスクールを</h3>
				<p>お子さんが習うためのスクールをご両親が探しているケースもあるでしょう。その場合はキッズ専用のスクールを選ぶのが良いです。キッズのレッスンは英語を教える能力以前に子供が好きでないと出来るものではないのです。英語を教える能力と子供に教える能力は別と考えてください。</p>
				<p>オンライン英会話スクールの中には、キッズ専用のスクールもいくつかありますのでまずはそれらを検討した方が良いでしょう。キッズ用コースを用意しているスクールでも良いと思いますが、&quot;キッズも可&quot;という程度の対応のスクールは、私なら避けたいと思います。</p>
